function geks_focusing_str(w,h,w2,h2,L0)
%a - side of geks, w - width, h - height, L - length
x0=['OFF'];
x1=[8 4 0;...
-w/2 -h/2 0;...
-w/2 h/2 0;...
w/2 h/2 0;...
w/2 -h/2 0;...
-w2/2 -h2/2 L0;...
-w2/2 h2/2 L0;...
w2/2 h2/2 L0;...
w2/2 -h2/2 L0];
x2=[4 0 1 5 4;...
4 1 2 6 5;...
4 2 3 7 6;...
4 3 0 4 7];
dlmwrite('geks_str_foc.off',x0,'')
dlmwrite('geks_str_foc.off',x1,'-append','delimiter',' ')
dlmwrite('geks_str_foc.off',x2,'-append','delimiter',' ')
end